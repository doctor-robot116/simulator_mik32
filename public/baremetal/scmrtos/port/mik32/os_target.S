.globl os_start
.globl os_context_switcher
.globl __init_system_timer


.section .ram_text

// extern "C" NAKED void os_start( stack_item_t* a_sp ) {
// Запуск первого процесса под управлением scmRTOS
// Вызывается в состоянии отключенных прерываний.
// Согласно ABI ilp32 первый аргумент передаётся в регистре a0 (a_sp)

.type os_start,@function
os_start:
  // сохраняем a0 в стеке
  add sp, sp, -4
  sw a0, 0(sp)
  // вызываем настройку системного таймера
  lui ra, %hi(__init_system_timer)
  jalr ra, %lo(__init_system_timer)(ra)
  // восстанавливаем sp из стека, где лежит сохранённый a0 (stack_item_t* a_sp)
  lw sp, 0(sp)
  // восстанавливаем содержимое регистров (тут интересен только mepc, который загружается
  // адресом передачи управления на точку входа функции exec() процесса, каковой адрес
  // помещается в стек процесса методом OS::TBaseProcess::init_stack_frame() при отработке
  // конструктора экземпляра процесса)
  // "восстанавливать" регистры общего назначения особого смысла нет
l_cold_start:
  // mstatus
  lw ra, 112(sp)
  csrw mstatus, ra
  // mepc
  lw ra, 116(sp)
  csrw mepc, ra
  // ra (x1)
  lw ra, 120(sp)
  // двигаем указатель стека после восстановления "контекста"
  add sp, sp, 124
  // возвращаем управление по адресу в mepc, так как возврат к процессу
  // при его запуске происходит атомарно с включением прерываний в инструкции mret
  // значений mstatus с поднятым битом MPIE заносится в стек методом TBaseProcess::init_stack_frame()
  // так что выполнение mret кроме передачи управления по адресу в mepc ещё и прерывания включит
  mret


// extern "C" void os_context_switcher( stack_item_t** Curr_SP, stack_item_t* Next_SP ) {
// Переключение с одного процесса на другой.
// Всегда вызывается в состоянии запрещённых прерываний (либо из обработчика прерываний либо в области
// действия критической секции).
// Функция вынесена в ОЗУ для минимизации задержек при её исполнении.
// Curr_SP содержит адрес, по которому нужно сохранить указатель стека (sp) после сохранения "контекста"
// текущего процесса (с которого переключаемся)
// Next_SP содержит значение для указателя стека процесса, на который переключаемся
// Согласно ABI ilp32 первый аргумент передаётся в регистре a0 (Curr_SP), второй - в a1 (Next_SP).

.type os_context_switcher,@function
os_context_switcher:
  // занимаем в текущем стеке место под сохранение "контекста"
  // x31..x4, mstatus, mepc, ra (x1) - 31 слово, 124 байта
  add sp, sp, -124
  // сохраняем "контекст" в текущем стеке
  sw x31,  0(sp)
  sw x30,  4(sp)
  sw x29,  8(sp)
  sw x28, 12(sp)
  sw x27, 16(sp)
  sw x26, 20(sp)
  sw x25, 24(sp)
  sw x24, 28(sp)
  sw x23, 32(sp)
  sw x22, 36(sp)
  sw x21, 40(sp)
  sw x20, 44(sp)
  sw x19, 48(sp)
  sw x18, 52(sp)
  sw x17, 56(sp)
  sw x16, 60(sp)
  sw x15, 64(sp)
  sw x14, 68(sp)
  sw x13, 72(sp)
  sw x12, 76(sp)
  sw x11, 80(sp)
  sw x10, 84(sp)
  sw x9,  88(sp)
  sw x8,  92(sp)
  sw x7,  96(sp)
  sw x6, 100(sp)
  sw x5, 104(sp)
  sw x4, 108(sp)
  // mstatus
  csrr t1, mstatus
  sw t1, 112(sp)
  // mepc
  csrr t1, mepc
  sw t1, 116(sp)
  // ra (x1)
  sw ra, 120(sp)
  // сохраняем указатель стека по адресу в Curr_SP
  sw sp, 0(a0)
  // загружаем новый указатель стека
  add sp, a1, 0
  // восстанавливаем регистры
  // ra (x1)
  lw ra, 120(sp)
  // если ra == 0 - то это первый запуск процесса
  beq x0, ra, l_cold_start
  //
  lw x31,  0(sp)
  lw x30,  4(sp)
  lw x29,  8(sp)
  lw x28, 12(sp)
  lw x27, 16(sp)
  lw x26, 20(sp)
  lw x25, 24(sp)
  lw x24, 28(sp)
  lw x23, 32(sp)
  lw x22, 36(sp)
  lw x21, 40(sp)
  lw x20, 44(sp)
  lw x19, 48(sp)
  lw x18, 52(sp)
  lw x17, 56(sp)
  lw x16, 60(sp)
  lw x15, 64(sp)
  lw x14, 68(sp)
  lw x13, 72(sp)
  lw x12, 76(sp)
  lw x11, 80(sp)
  lw x10, 84(sp)
  lw x9,  88(sp)
  lw x8,  92(sp)
  lw x7,  96(sp)
  lw x6, 100(sp)
  lw x5, 104(sp)
  // Для загрузки mstatus и mepc воспользуемся регистром x4 (tp), который восстановим позже.
  // mstatus
  lw x4, 112(sp)
  csrw mstatus, x4
  // Если os_context_switcher в процессе, на который переключаемся, был вызван
  // из обработчика прерываний, то восстановленное значение mepc позволит передать
  // управление куда надо при выходе из обработчика прерываний (а значение в ra
  // как раз и вернёт управление в обработчик прерывания). В этом случае значение
  // в регистре mstatus позволит включить прерывания при выходе из обработчика прерывания.
  // При вызове os_context_switcher из кода значения mstatus и mepc рояли не играют,
  // так как отключение прерываний и влючение их снова обеспечивает критическая
  // секция, в области действия которой вызывается планировщик процессов, который
  // в свою очередь может вызвать os_context_switcher.s
  // mepc
  lw x4, 116(sp)
  csrw mepc, x4
  // восстановим x4
  lw x4, 108(sp)
  // двигаем указатель стека после восстановления "контекста"
  add sp, sp, 124
  // возвращаем управление по адресу в ra
  ret // синоним для jalr x0, 0(x1)
